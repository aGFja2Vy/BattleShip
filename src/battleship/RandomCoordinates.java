package battleship;

/**
 *
 * @author 956040525
 */
public class RandomCoordinates {
    
    public final int x;
    public final int y;

    public RandomCoordinates(int x, int y) 
    {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
